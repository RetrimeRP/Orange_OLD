local notify = DarkRP.notify
local function PropWhitelist(ply, mdl)
	if ply:Team() == TEAM_FSG or ply:IsSupervisor() then return end -- ИЗМЕНЕНО 15.01.2022

	-- mdl
	mdl = lower(mdl)
	mdl = Replace(mdl, "\\", "/")
	mdl = gsub(mdl, "[\\/]+", "/")

	-- whitelist
	if not whitelist[mdl] then
		DarkRP.notify(ply, 0, 5, "Разрешённые пропы находятся в соответствующей вкладке!")
		return false
	end
end


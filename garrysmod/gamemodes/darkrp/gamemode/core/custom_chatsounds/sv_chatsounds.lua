local random = math.random -- ИЗМЕНЕНО 05.02.2022
hook.Add("PostPlayerSay", "ChatSounds", function(ply, text)
	local function GetSoundlist()
    
		local mdl = ply:GetModel()
	
		if mpfmodels[mdl] then
			return mpf
		end
	
		if otamodels[mdl] then
			return ota
		end
	
		if femalemodels[mdl] then
			return female
		end
	
		if malemodels[mdl] then
			return male
		end
	
		if hecumodels[mdl] then
			return hecu
		end
	
		if vortigauntmodels[mdl] then
			return vortigaunt
		end
	
		if loyalistmodels[mdl] then
			return male
		end

		if negrmodels[mdl] then
			return negr
		end

		if germanmodels[mdl] then
			return german
		end
	
		return false
	end
	local soundlist = GetSoundlist()

	if soundlist then
		if ply.NextChatSound and ply.NextChatSound > CurTime() then return end

		local sounds = soundlist[text]

		if sounds then
			--local sound = sounds[random(#sounds)]

			ply:EmitSound(sounds[random(#sounds)], 80, 100)
			ply.NextChatSound = CurTime() + 2
		end
	end
end)